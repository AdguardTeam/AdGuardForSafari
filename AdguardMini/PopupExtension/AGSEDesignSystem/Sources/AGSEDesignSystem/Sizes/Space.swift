import Foundation

enum Space {
    /// 0pt space
    static let zero: CGFloat = 0

    /// 2pt space
    static let tiny: CGFloat = 2

    /// 8pt space
    static let small: CGFloat = 8

    /// 10pt space
    static let smallPlus: CGFloat = 10

    /// 12pt space
    static let compact: CGFloat = 12

    /// 16pt space
    static let regular: CGFloat = 16
}
