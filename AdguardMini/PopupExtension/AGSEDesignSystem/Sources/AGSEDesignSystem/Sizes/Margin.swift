import Foundation

enum Margin {
    /// 0pt margin
    static let zero: CGFloat = 0

    /// 8pt margin
    static let small: CGFloat = 8

    /// 16pt margin
    static let regular: CGFloat = 16
    /// 24pt margin
    static let expanded: CGFloat = 24

    /// 36pt margin
    static let extraLarge: CGFloat = 36
}
