import Foundation

enum Size {
    /// 2pt space
    static let tiny = CGSize(side: 2)

    /// 4pt side size
    static let extraSmall = CGSize(side: 4)

    /// 8pt side size
    static let small = CGSize(side: 8)

    /// 12pt side size
    static let compact = CGSize(side: 12)

    /// 16pt side size
    static let regular = CGSize(side: 16)

    /// 20pt side size
    static let medium = CGSize(side: 20)

    /// 24pt side size
    static let expanded = CGSize(side: 24)

    /// 32pt side size
    static let large = CGSize(side: 32)

    /// 36pt side size
    static let extraLarge = CGSize(side: 36)

    /// 40pt side size
    static let huge = CGSize(side: 40)

    /// 48pt side size
    static let extraHuge = CGSize(side: 48)
}
